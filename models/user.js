const { Schema, mongoose } = require('./base');

const userSchema = new Schema({
    firstName: {
        type: String,
        required: true,
    },
    lastName: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    password: {
        type: String,
        required: true,
        select: false,
    },
    role: {
        type: String,
    }
});

const UserModel = new mongoose.model('users', userSchema);

module.exports = UserModel;